-(void)drawRect:(CGRect)rect{
    self.hidden = YES;
    
    CGRect newRect = [self.window.rootViewController.view convertRect:rect fromView:self];
    
    UIImage *windowScreenShotImage = [self.window.rootViewController.view rn_screenshot];
    
    CGImageRef imageRef = CGImageCreateWithImageInRect(windowScreenShotImage.CGImage, newRect);
    
    
    UIImage *bluredImg = [UIImage imageWithCGImage:imageRef];
    
    
    
    bluredImg = [bluredImg applyBlurWithRadius:_blurRadius tintColor:_maskColor saturationDeltaFactor:_saturationDeltaFactor maskImage:nil];
    [bluredImg drawAtPoint:rect.origin];
    
    bluredImg = nil;
    windowScreenShotImage = nil;
    CGImageRelease(imageRef);
    
//    [windowScreenShotImage drawInRect:rect];
    self.hidden = NO;

}

